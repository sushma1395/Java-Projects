        checkingaccountinformation.setBankRoutingnumber1(txtBankRoutingNumber1.getText());
        checkingaccountinformation.setAccountBalance1(txtAccountBalance1.getText());
      
        //Binding the User Input to saving account information Object
        savingaccountinformation.setBankAccountnumber(txtBankAccountNumber.getText());
        savingaccountinformation.setBankRoutingnumber(txtBankRoutingNumber.getText());
        savingaccountinformation.setAccountBalance(txtAccountBalance.getText());
        
        //Binding the User Input to Driver's License Object
        
        driverslicenseinformation.setLicenseNumber(txtLicensenumber.getText());
